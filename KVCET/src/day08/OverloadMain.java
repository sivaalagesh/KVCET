package day08;

public class OverloadMain 
{

	public static void main(int c) 
	{
		System.out.println("Main method with string array Argument");

	}
	
	public static void main(String[] args)
	{
		System.out.println("Main method with zero argument");
	}
	
	public static void main(double a, int b)
	{
		System.out.println("Main method with double argument");
	}
	

}
